# Define the required variables for simple interest calculation
principal = 1000  # P (initial investment)
rate = 0.05       # R (annual interest rate as a decimal)
time = 3          # T (time in years)

# Calculate the simple interest using the formula: I = P * R * T
interest = principal * rate * time

# Print the calculated interest in the specified format: "The simple interest is: [interest]"
print(f"The simple interest is: {interest}")
